        private void Ecidui(object moleileucucisUgofe, EventArgs deisiko)
        {
            this._saesueqadau.Enabled = false;
            if ((this._hoqaiqaekou.Items.Count <= 0) || (this._hoqaiqaekou.SelectedIndex < 0))
            {
                MessageBox.Show("There is no plugins activated or selected.");
                this._saesueqadau.Enabled = true;
            }
            else
            {
                Plugin plugin2 = new Plugin {
                    PathToPluginFile = Application.StartupPath + @"\Plugins\" + this._hoqaiqaekou.SelectedItem
                };
                plugin2.ShowConfigurationPlugin();
                this._saesueqadau.Enabled = true;
            }
        }

